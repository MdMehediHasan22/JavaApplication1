            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/sampledb","root","Mysql@root3306");
           // Use a PreparedStatement to prevent SQL injection
        String query = "INSERT INTO sample (id, name, price, category) VALUES (?, ?, ?, ?)";
        PreparedStatement pstmt = con.prepareStatement(query);
        
        // Set parameters
        pstmt.setString(1, txtId.getText());  // Assuming 'id' is a string
