    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
       try {
         // Load the JDBC driver
        Class.forName("com.mysql.cj.jdbc.Driver");
        
        // Establish the database connection
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/sampledb", "root", "Mysql@root3306");
        
        // Prepare the SQL query
        String query = "SELECT * FROM sample WHERE id = ?";
        PreparedStatement statement = con.prepareStatement(query);
        
        // Set the parameter for the query
        statement.setString(1, txtId.getText());
        
        // Execute the query and get the result set
        ResultSet set = statement.executeQuery();
        
        // Process the result set
        if (set.next()) {
            txtName.setText(set.getString("name"));
            txtPrice.setText(set.getString("price"));
            comboCat.setSelectedItem(set.getString("category"));
        } else {
            // Handle the case where no record is found
            JOptionPane.showMessageDialog(null, "No record found with ID: " + txtId.getText());
        }
} catch (Exception ex) {
        // Display an error message if an exception occurs
        JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
    }
        
    
    }//GEN-LAST:event_btnViewActionPerformed

